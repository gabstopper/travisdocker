"""
SMC API uses major and minor versions but the API itself can change from maintenance
release to release, i.e. 6.1.1 to 6.1.2. This will check the actual SMC version to 
provide checks if minimum SMC version is required
"""
def is_min_required_smc_version(version1, version2):
    # Version1 is current version
    # Version2 is required version
    version_str = version1.split(' ')
    version_parts = version_str[0].split('.')
    if len(version_parts) == 3: #SMC Versions are 3 parts, i.e. 6.1.0
        smc_minor_version = int(version_parts.pop())
        smc_major_version = float('.'.join(version_parts))
    req_major, req_minor = _version_tuple(version2)
    if smc_major_version >= req_major:
        # Is minor version minimum...
        if smc_minor_version >= req_minor:
            return True
        else:
            return False
    else:
        return False
             
def _version_tuple(version):
    # Expecting 3 digit version, i.e. 6.1.0, 6.1.1, etc.
    # Return (major, minor) -> (float, int)
    version_parts = version.split('.')
    minor_version = int(version_parts.pop())
    major_version = float('.'.join(version_parts))
    return (major_version, minor_version) 


